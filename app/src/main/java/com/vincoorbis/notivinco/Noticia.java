package com.vincoorbis.notivinco;

public class Noticia {
    String id;
    String title;
    String content;
    String date;
    String image;

    public Noticia(String id, String title, String content, String date, String image) {
        this.id = id;
        this.title = title;
        this.content = content;
        this.date = date;
        this.image = image;
    }
}
